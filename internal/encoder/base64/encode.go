// Encode encodes a number into a base64 string
func Encode(s *[11]byte, n uint64, d func() [64]byte) {
	var digits = d()

	var ln uint64

	for i := 0; i < 8; i++ {
		ln, n = ln<<8|n&0xFF, n>>8
	}

	for i := 0; i <= 10; i++ {
		s[i], ln = digits[ln>>58], ln<<6
	}

}
